# Python supports different types of operators.
# Let's practice arithmetic, comparison, and logical operators.

# ---------- Arithmetic Operators ----------
a = 10
b = 3

print("Addition (a + b):", a + b)          # 10 + 3 = 13
print("Subtraction (a - b):", a - b)       # 10 - 3 = 7
print("Multiplication (a * b):", a * b)    # 10 * 3 = 30
print("Division (a / b):", a / b)          # 10 / 3 = 3.33
print("Floor Division (a // b):", a // b)  # 10 // 3 = 3 (removes decimal)
print("Modulus (a % b):", a % b)           # 10 % 3 = 1 (remainder)
print("Exponent (a ** b):", a ** b)        # 10^3 = 1000

# ---------- Comparison Operators ----------
# These return True or False
print("Is a > b?", a > b)                  # True
print("Is a == b?", a == b)                # False
print("Is a != b?", a != b)                # True

# ---------- Logical Operators ----------
x = True
y = False

print("x and y:", x and y)                 # True AND False = False
print("x or y:", x or y)                   # True OR False = True
print("not x:", not x)                     # NOT True = False
